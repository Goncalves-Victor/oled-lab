export const CHAR_WIDTH = 6;  // 5 colunas + 1 de espacamento
export const CHAR_HEIGHT = 8; // 7 px + 1 de espacamento

/**
 * Desenha 1 caractere no framebuffer na posicao (x, y).
 * size amplia em escala inteira (1, 2, 3...). Cada pixel da fonte vira um
 * bloco size x size, como na Adafruit_GFX.
 */
export function drawChar(
  fb: Framebuffer,
  x: number,
  y: number,
  ch: string,
  color: PixelColor,
  size: number,
): void {
  const code = ch.charCodeAt(0);
  const index = code - 0x20;
  // Caractere fora da tabela vira espaco (nao quebra).
  const glyph = index >= 0 && index < FONT_5x7.length ? FONT_5x7[index] : FONT_5x7[0];

  for (let col = 0; col < 5; col++) {
    let bits = glyph[col];
    for (let row = 0; row < 8; row++) {
      if (bits & 0x1) {
        if (size === 1) {
          fb.setPixel(x + col, y + row, color);
        } else {
          // bloco quadrado para o pixel ampliado
          for (let sy = 0; sy < size; sy++) {
            for (let sx = 0; sx < size; sx++) {
              fb.setPixel(x + col * size + sx, y + row * size + sy, color);
            }
          }
        }
      }
      bits >>= 1;
    }
  }
}

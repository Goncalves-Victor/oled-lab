// (glcdfont). Cada caractere ocupa 5 colunas; cada numero abaixo representa
// UMA COLUNA, onde o bit menos significativo (1) e o pixel de CIMA e o bit 6
// (64) e o pixel de baixo (fonte de 7 pixels de altura dentro de uma celula
